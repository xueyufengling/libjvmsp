	public static final String to_string(byte[] bytecode)
	{
		StringBuilder sb = new StringBuilder();
		for (int bci = 0; bci < bytecode.length; ++bci)
		{
			byte bc = bytecode[bci];
			sb.append(name(bc));
			int ins_len = classfile_constants.instruction_length(bc);
			switch (ins_len)
			{
			case 0:
				sb.append(' ');
				break;
			case 1:
				sb.append('\n');
				break;
			default:
				sb.append(' ');
				for (int op = 1; op < ins_len; ++op)
				{
					++bci;
					sb.append(String.format("0x%02x", bytecode[bci]));
					if (op == ins_len - 1)
					{
						sb.append('\n');
						break;
					}
					sb.append(", ");// 不是最后一个参数则用逗号分隔
				}
				break;
			}
		}
		int last = sb.length() - 1;
		if (sb.charAt(last) == '\n')
			sb.deleteCharAt(last);// 删除结尾多余的'\n'
		return sb.toString();
	}

	/**
	 * 运行时在ConstMethod对象中字节码取常量池索引操作数。<br>
	 * 在.class文件中，常量池索引固定为大端存储，且是ConstantPool的索引。<br>
	 * 但运行时，取得的是ConstantPoolCache中的已决议条目的索引，即cp_cache_idx，而非ConstantPool中的索引，并且该索引是本地字节序的，不是固定大端。<br>
	 * 通过cp_cache_idx获取到对应的ConstantPoolCacheEntry条目（或ResolvedXXXEntry）后才从中获取cp_idx。<br>
	 * https://github.com/openjdk/jdk/blob/jdk-21%2B35/src/hotspot/share/interpreter/zero/bytecodeInterpreter.cpp#L2428
	 * InterpreterRuntime::resolve_from_cache() https://github.com/openjdk/jdk/blob/jdk-25%2B36/src/hotspot/share/interpreter/interpreterRuntime.cpp#L989
	 * 
	 * @param op1
	 * @param op2
	 * @return
	 */
	public static final int runtime_cp_cache_idx(byte op1, byte op2)
	{
		if (ByteOrder.nativeOrder() == ByteOrder.LITTLE_ENDIAN)
		{
			return (op2 << 8) | (op1 & cxx_type.uint8_t_mask);
		}
		else
		{
			return (op1 << 8) | (op2 & cxx_type.uint8_t_mask);
		}
	}

	/**
	 * 从native内存中取运行时常量池索引
	 * 
	 * @param code_base
	 * @param ins_bci   具有常量池索引操作数的字节码索引
	 * @return
	 */
	public static final int fetch_cp_cache_idx(long code_base, int ins_bci)
	{
		return unsafe.read_uint16_t(code_base + ins_bci + 1);
	}

	public static final int fetch_cp_cache_idx(ConstMethod cm, int ins_bci)
	{
		return fetch_cp_cache_idx(cm.code_base(), ins_bci);
	}

	public static final int fetch_cp_cache_idx(byte[] bc_arr, int ins_bci)
	{
		return runtime_cp_cache_idx(bc_arr[ins_bci + 1], bc_arr[ins_bci + 2]);
	}

	/**
	 * 查找字节码第n次出现的索引
	 * 
	 * @param bc_arr
	 * @param bc
	 * @param n
	 * @return
	 */
	public static final int find_bci(byte[] bc_arr, byte bc, int n)
	{
		assert is_valid(bc) : "invalid bytecode '" + bc + "'";
		return algorithms.index_of(bc_arr, bc, n);
	}

